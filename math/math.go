package math

func Sum(number1 float64, number2 float64) float64 {
	return number1 + number2
}

func Substract(number1 float64, number2 float64) float64 {
	return number1 - number2
}

func Multiply(number1 float64, number2 float64) float64 {
	return number1 * number2
}

func Division(number1 float64, number2 float64) float64 {
	return number1 / number2;
}
